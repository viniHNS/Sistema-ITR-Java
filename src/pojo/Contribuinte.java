package pojo;

public class Contribuinte {
    private int idContribuinte;
    private String nomeContribuinte;
    private String telefoneContribuinte;
    private String cpfCnpjContribuinte;
    private String logradouroContribuinte;
    private String numeroContribuinte;
    private String bairroContribuinte;
    private String complementoContribuinte;
    private char ativoContribuinte;
    private int idCidade;


    public int getIdContribuinte() {
        return this.idContribuinte;
    }

    public void setIdContribuinte(int idContribuinte) {
        this.idContribuinte = idContribuinte;
    }

    public String getNomeContribuinte() {
        return this.nomeContribuinte;
    }

    public void setNomeContribuinte(String nomeContribuinte) {
        this.nomeContribuinte = nomeContribuinte;
    }

    public String getTelefoneContribuinte() {
        return this.telefoneContribuinte;
    }

    public void setTelefoneContribuinte(String telefoneContribuinte) {
        this.telefoneContribuinte = telefoneContribuinte;
    }

    public String getCpfCnpjContribuinte() {
        return this.cpfCnpjContribuinte;
    }

    public void setCpfCnpjContribuinte(String cpfCnpjContribuinte) {
        this.cpfCnpjContribuinte = cpfCnpjContribuinte;
    }

    public String getLogradouroContribuinte() {
        return this.logradouroContribuinte;
    }

    public void setLogradouroContribuinte(String logradouroContribuinte) {
        this.logradouroContribuinte = logradouroContribuinte;
    }

    public String getNumeroContribuinte() {
        return this.numeroContribuinte;
    }

    public void setNumeroContribuinte(String numeroContribuinte) {
        this.numeroContribuinte = numeroContribuinte;
    }

    public String getBairroContribuinte() {
        return this.bairroContribuinte;
    }

    public void setBairroContribuinte(String bairroContribuinte) {
        this.bairroContribuinte = bairroContribuinte;
    }

    public String getComplementoContribuinte() {
        return this.complementoContribuinte;
    }

    public void setComplementoContribuinte(String complementoContribuinte) {
        this.complementoContribuinte = complementoContribuinte;
    }

    public char getAtivoContribuinte() {
        return this.ativoContribuinte;
    }

    public void setAtivoContribuinte(char ativoContribuinte) {
        this.ativoContribuinte = ativoContribuinte;
    }

    public int getIdCidade() {
        return this.idCidade;
    }

    public void setIdCidade(int idCidade) {
        this.idCidade = idCidade;
    }

}
