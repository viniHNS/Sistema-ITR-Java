package pojo;

public class Pais {
    private int idPais;
    private String nomePais;
    private char ativoPais;


    public int getIdPais() {
        return this.idPais;
    }

    public void setIdPais(int idPais) {
        this.idPais = idPais;
    }

    public String getNomePais() {
        return this.nomePais;
    }

    public void setNomePais(String nomePais) {
        this.nomePais = nomePais;
    }

    public char getAtivoPais() {
        return this.ativoPais;
    }

    public void setAtivoPais(char ativoPais) {
        this.ativoPais = ativoPais;
    }
    
}
