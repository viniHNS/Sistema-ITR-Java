package pojo;

public class Propriedade {
    private int idPropriedade;
    private String nomePropriedade;
    private String nirfPropriedade;
    private String areaTotalPropriedade;
    private String parametroSelecaoPropriedade;
    private String statusPropriedade;
    private char ativoPropriedade;
    private int idContribuinte;


    public int getIdPropriedade() {
        return this.idPropriedade;
    }

    public void setIdPropriedade(int idPropriedade) {
        this.idPropriedade = idPropriedade;
    }

    public String getNomePropriedade() {
        return this.nomePropriedade;
    }

    public void setNomePropriedade(String nomePropriedade) {
        this.nomePropriedade = nomePropriedade;
    }

    public String getNirfPropriedade() {
        return this.nirfPropriedade;
    }

    public void setNirfPropriedade(String nirfPropriedade) {
        this.nirfPropriedade = nirfPropriedade;
    }

    public String getAreaTotalPropriedade() {
        return this.areaTotalPropriedade;
    }

    public void setAreaTotalPropriedade(String areaTotalPropriedade) {
        this.areaTotalPropriedade = areaTotalPropriedade;
    }

    public String getParametroSelecaoPropriedade() {
        return this.parametroSelecaoPropriedade;
    }

    public void setParametroSelecaoPropriedade(String parametroSelecaoPropriedade) {
        this.parametroSelecaoPropriedade = parametroSelecaoPropriedade;
    }

    public String getStatusPropriedade() {
        return this.statusPropriedade;
    }

    public void setStatusPropriedade(String statusPropriedade) {
        this.statusPropriedade = statusPropriedade;
    }

    public char getAtivoPropriedade() {
        return this.ativoPropriedade;
    }

    public void setAtivoPropriedade(char ativoPropriedade) {
        this.ativoPropriedade = ativoPropriedade;
    }

    public int getIdContribuinte() {
        return this.idContribuinte;
    }

    public void setIdContribuinte(int idContribuinte) {
        this.idContribuinte = idContribuinte;
    }

}
